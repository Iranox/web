  <h2>Das Projekt</h2>
  <p>Auch in Leipzig werden die ablaufenden Gentrifizierungsprozesse als Teil von Stadtgestaltung intensiv beobachtet. Insbesondere gibt es bereits auch eine Reihe von Datenerhebungen zu diesem Thema. In diesem Projekt wurde im Sommersemester 2014 die Qualität dieser Daten von einem interdisziplinären studentischen Projektteam am Institut für Informatik der Universität Leipzig unter der Leitung von Prof. Dr. Hans-Gert Gräbe untersucht und eine Verbindung mit anderen Daten aus dem <a href="http://www.leipzig-data.de" target="_blank">Leipzig Data Projekt</a> hergestellt. Das Projekt ist ein Spin-off eines größeren Vorhabens, das auf <a href="http://www.einundleipzig.de/">einundleipzig.de</a> genauer erläutert wird.</p>
  <p>Wir haben uns mit verschiedenen Aspekten dieser Gentrifizierungsprozesse in Leipzig beschäftigt, dazu Daten auf Ortsteilebene für verschiedene Jahre gesammelt,  und diese in zwei Karten visualisiert.</p>
